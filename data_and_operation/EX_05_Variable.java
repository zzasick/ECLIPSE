
public class EX_05_Variable {

	public static void main(String[] args) {
		
		int a = 1; // Number -> integer ... -2, -1, 0, 1, 2 ...
		System.out.println(a); // 1
		
		double b = 1.1; // real number -> double ... -2.0, -1.0, 0, 1.0, 2.0 ... (infinite)
		System.out.println(b); // 1.1
		
		String c = "Hello World"; // words -> string
		System.out.println(c); // Hello World

		// We have to indicate the type of "Variable" such as integer, double, and string.
	}

}
