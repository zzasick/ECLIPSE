
public class EX_03_StringApp {

	public static void main(String[] args) {

		System.out.println("Hello World"); // String - collection of characters
		System.out.println('H'); // Character - express only 1 word
		System.out.println("H"); // a word, but a string
		
		System.out.println("Hello "
				+"World"); // no new line
		
		// \n means new line
		System.out.println("Hello \nWorld");
		System.out.println("Hello \"World\"");// Hello "World" // 
		// \ before a word is called 'escape'
	}

}
 